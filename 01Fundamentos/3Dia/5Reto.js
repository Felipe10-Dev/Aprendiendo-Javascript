// Definimos Variables
let dia = 1; 

// Estructura Condicional Para Saber Que Dia De La Semana

switch (dia) {


    case 1: 
    console.log("Domingo"); 
    break; 

    case 2: 
    console.log("Lunes");
    break; 
    
    case 3: 
    console.log("Martes"); 
    break; 

    case 4: 
    console.log("Miercoles"); 
    break; 

    case 5: 
    console.log("Jueves");
    break; 
    
    case 6: 
    console.log("Viernes");
    break; 
    
    case 7: 
    console.log("Sabado");
    break; 
    
    default: 
    console.log("Numero Invalido"); 


}